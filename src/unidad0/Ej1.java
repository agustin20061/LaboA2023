package unidad0;

public class Ej1 {
    public static void main(String[] args) {
        int n = 2;
        double a = 2.5;
        char c = 'z';

        System.out.println("El valor N: " + n);
        System.out.println("El valor A: " + a);
        System.out.println("El valor C: " + c);
        System.out.println("La suma de n+a es: " + (n+a));
        System.out.println("La diferencia de a-n es: " + (a-n));
        System.out.println("El valor numerico de c es: " + (int)c);
    }
}
